// função padrão
function soma(a,b){
    return a+b
}
console.log(soma(2,3))

function subtracao(a,b){
    return a-b
}
console.log(subtracao(2,3))

function multiplicacao(a,b){
    return a*b
}
console.log(multiplicacao(2,3))

function divisao(a,b){
    return a/b
}
console.log(divisao(2,3))

